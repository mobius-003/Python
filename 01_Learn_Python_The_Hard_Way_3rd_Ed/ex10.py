tabby_cat = "\tI'm tabbed in." # variable tabby_cat
# \t - insert a tab here
persian_cat = "I'm split\non a line. " # variable persian_cat (prints on two lines)
# \n  - go to the next line
backslash_cat = "I'm \\ a \\ cat." # variable backslash_cat (prints with two \\)
# \\ - insert a simple backslash here
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
""" # variable fat_cat presented as a sting as a listing (3 tabs) display

print(tabby_cat) # prints tabby_cat
print(persian_cat)  #prints persian_cat
print(backslash_cat) #prints backslash_cat
print(fat_cat) #prints fat_cat
